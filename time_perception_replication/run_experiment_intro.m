function run_experiment_intro(replication_type, window, back_color, black, screenXpixels)
% Runs the experiment intro, identical but customizable for each
% experiment! 
    statement = 'In the task, you will be presented with an image for a period of time.  Pay attention to how long the image is presented on the screen. \n \n Press the spacebar to continue';
    display_screen_text(statement, ...
            window, ...
            back_color, ... 
            black, ...
            screenXpixels);
    
    statement = 'When the image is removed from the screen, your task will be to replicate how long the image felt like it was on screen. \n \n Press the spacebar to continue';
    display_screen_text(statement, ...
            window, ...
            back_color, ... 
            black, ...
            screenXpixels);
    if replication_type == "start_stop"
        statement = 'When prompted, click the spacebar to start the interval, and click the spacebar again to indicate when you think the image was removed from screen. \n \n Press the spacebar to continue.';
        display_screen_text(statement, ...
            window, ...
            back_color, ... 
            black, ...
            screenXpixels);
    elseif replication_type == "hold"
        statement = 'When prompted, hold down on the spacebar to indicate how long it felt like the image was onscreen. \n \n Press the spacebar to continue';
        display_screen_text(statement, ...
            window, ...
            back_color, ... 
            black, ...
            screenXpixels);
    elseif replication_type == "stop"
        statement = 'Soon after the image leaves the screen, a black + sign will appear on screen.';
        display_screen_text(statement, ...
            window, ...
            back_color, ... 
            black, ...
            screenXpixels);
        statement = 'The appearance of the + sign indicates the start of the interval. Click the spacebar to indicate when the interval should end.';
        display_screen_text(statement, ...
            window, ...
            back_color, ... 
            black, ...
            screenXpixels);
    end

    display_screen_text('You will begin with several practice trials to get used to the task. You can press the escape key at any time to exit the experiment. \n \n Press the spacebar to continue.', ...
            window, ...
            back_color, ... 
            black, ...
            screenXpixels);
    statement = 'The training segment will begin when you press the spacebar. \n \n Press the spacebar to begin.';
    display_screen_text(statement, ...
            window, ...
            back_color, ... 
            black, ...
            screenXpixels);
end 